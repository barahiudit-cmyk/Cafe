export const getFeaturedItems = (): MenuItem[] =>
  menuItems.filter((item) => item.featured && item.available);

export const getItemBySlug = (slug: string): MenuItem | undefined =>
  menuItems.find((item) => item.slug === slug);

export const getItemsByCategory = (category: string): MenuItem[] =>
  category === 'all'
    ? menuItems.filter((i) => i.available)
    : menuItems.filter((i) => i.category === category && i.available);
